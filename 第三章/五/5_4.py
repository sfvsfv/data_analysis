# 正索引
mylist = ["张三", "小李", "张三", "小明"]
print(mylist[1:3])

# 负索引
mylist = ["张三", "小李", "张三", "小明"]
print(mylist[-3:-1])

# 负的省略索引
mylist = ["张三", "小李", "张三", "小明"]
print(mylist[-3:])

# 正的省略索引
mylist = ["张三", "小李", "张三", "小明"]
print(mylist[:3])
